package pertemuan3;

public class Pegawai02 {
	
	String no;
	String nama;
	double gaji;
	
	void setNo(String no) {
		this.no = no;
	}
	
	String getNo() {
		return no;
	}
	
	void setName(String nama) {
		this.nama = nama;
	}
	
	String getName() {
		return nama;
	}
	
	void setGaji(double gaji) {
		this.gaji = gaji;
	}
	
	double getGaji() {
		return gaji;
	}

}
